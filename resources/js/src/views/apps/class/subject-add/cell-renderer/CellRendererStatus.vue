<template>
    <div>
     <vs-button @click="AddClassSubjects(params.value)"> Add</vs-button>
                     </div>
    </div>
</template>

<script>
import axios from "@/axios.js";
export default {
  name: 'CellRendererActions',
  methods: {
   
    AddClass () {
      this.$store.dispatch("classManagement/AddClassSubjects", this.params.data.id)
        .then(()   => { this.showDeleteSuccess() })
        .catch(err => { console.error(err)       })
    },
    showDeleteSuccess () {
      this.$vs.notify({
        color: 'success',
        title: 'Deleted',
        text: 'Data deleted successfully'
      })
    }
  }
}
</script>
